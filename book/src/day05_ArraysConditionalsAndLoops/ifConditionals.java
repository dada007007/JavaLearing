package day05_ArraysConditionalsAndLoops;

// if条件语句 if用于多个范围内的选择
public class ifConditionals {
    public static void main(String[] args) {
        int x = 5;
        int y = 10;
        if (x < y) {
            System.out.println("x is smaller than y. ");
        } else {
            System.out.println("x is larger than y.");
        }
    }
}
