package com.itheima.array;

public class Demo6 {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        System.out.println(a == 5);
        System.out.println(a != 5);
        System.out.println(a >= 5);
        System.out.println(a <= 5);
        System.out.println(a == b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
